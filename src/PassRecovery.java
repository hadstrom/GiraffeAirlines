    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (jButton1.getText().equals("Submit")){//If we are in the first state
            //Establish connection to JDBC
            try{
                Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/data","root", "password");
                Statement smt = con.createStatement();
                //Check if id exists
                ResultSet result = smt.executeQuery("SELECT COUNT(*) FROM customer where id = '"+jTextField1.getText().trim()+"'");
                result.next();
                if(result.getInt(1)>0) {//If id exists
                    result = smt.executeQuery("SELECT * FROM customer where id = '"+jTextField1.getText().trim()+"'");
                    result.next();
                    //Save the answer and password into class variables
                    answer = result.getString("answer");
                    pass = result.getString("password");
                    //Show the question label with the question
                    jLabel1.setVisible(true);
                    jLabel1.setText("Question: " + result.getString("question"));
                    //Change the second label to "answer" and the text on the submit button
                    jLabel2.setText("Answer:");
                    jButton1.setText("Recover your password");
                    jTextField1.setText("");
                }else JOptionPane.showMessageDialog(null, "ID does not exist");//Warn that id doesn't exist
            } catch(Exception exc){
                System.out.println(exc);
            }
        } else {//If we are in the second state(after receive the id)
            if(answer.equalsIgnoreCase(jTextField1.getText())){//If user answers the security question correctly
                //Show the password and hide everything else
                jLabel2.setText("Your password: " + pass);
                jTextField1.setVisible(false);
                jButton1.setVisible(false);
                jLabel1.setVisible(false);
            }else JOptionPane.showMessageDialog(null, "Incorrect answer");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

